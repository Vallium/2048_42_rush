void	move_r(int tab[4][4], int y)
{
	int x;

	x = 3;
	while (--x > -1)
		if (tab[y][x + 1] == 0)
		{
			tab[y][x + 1] = tab[y][x];
			tab[y][x] = 0;
		}
}
